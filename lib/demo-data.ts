export function getDemoQuotes(): StockQuote[] {
  return RAW.map(([ticker, name, price, changePct, vol, avgVol, high52, low52, beta, marketCap]) => ({
    ticker: ticker as string,
    name: name as string,
    sector: getSectorForTicker(ticker as string),
    price: price as number,
    change: Math.round((price as number) * (changePct as number) / 100 * 100) / 100,
    changePercent: changePct as number,
    volume: vol as number,
    avgVolume: avgVol as number,
    high52w: high52 as number,
    low52w: low52 as number,
    beta: beta as number,
    marketCap: marketCap as number,
    pctFromLow: Math.round(((price as number) - (low52 as number)) / (low52 as number) * 1000) / 10,
    pctFromHigh: Math.round(((high52 as number) - (price as number)) / (high52 as number) * 1000) / 10,
    volumeRatio: Math.round((vol as number) / (avgVol as number) * 10) / 10,
  }));
}

